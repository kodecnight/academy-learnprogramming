package academy.learnprogramming.section04;

public class StatementsWhitespacesAndIndenting {
    public static void main(String[] args) {
        int myVariable = 50;
        myVariable++;
        myVariable--;
        System.out.println("The above are example of statements");
        System.out.println("This is also another example" +
                "for statement" +
                ", as this can be broken into multi line");

        int myNumber = 100;
        myNumber++;
        myNumber--;
        System.out.println("Can also be grouped in single line, best practise is to have them in separate lines");
    }
}
